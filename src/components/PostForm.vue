<template>
  <div id="app">
    <input id="title" type="text" v-model="title" placeholder="タイトル" />
    <div id="main">
      <mavon-editor v-model="value" :toolbars="markdownOption" language="ja" />
    </div>
    <button id="postButton" v-on:click="post">投稿</button>
  </div>
</template>

<script>
import { db } from "@/firebase";

export default {
  name: "app",
  data() {
    return {
      title: "",
      value: "",
      markdownOption: {
        bold: true,
        italic: true,
        header: true,
        underline: true,
        strikethrough: true,
        mark: true,
        superscript: true,
        subscript: true,
        quote: true,
        ol: true,
        ul: true,
        link: true,
        code: true,
        table: true,
        fullscreen: true,
        readmodel: true,
        htmlcode: true,
        help: true,
        undo: true,
        redo: true,
        navigation: true,
        alignleft: true,
        aligncenter: true,
        alignright: true,
        subfield: true,
        preview: true
      }
    };
  },
  methods: {
    post: function() {
      this.$dialog
        .confirm(
          {
            title: "最終確認",
            body: "本当に投稿してもよろしいですか？"
          },
          {
            okText: "はい",
            cancelText: "キャンセル"
          }
        )
        .then(() => {
          const post = {
            title: this.title,
            value: this.value,
            date: Date.now()
          };
          if (post.title && post.value) {
            this.title = "";
            this.value = "";
            db.collection("posts").add(post);
          }
        })
        .catch(() => {
          console.log("Clicked on cancel");
        });
    }
  }
};
</script>
